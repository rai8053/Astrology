export function detectBrowserLanguage(): Language {
  try {
    const lang = navigator.language?.split('-')[0]?.toLowerCase() || 'en';
    const supported: Language[] = ['en', 'hi', 'bn', 'es', 'pt', 'fr', 'de', 'ja', 'zh', 'ar'];
    return supported.includes(lang as Language) ? lang as Language : 'en';
  } catch { return 'en'; }
}

